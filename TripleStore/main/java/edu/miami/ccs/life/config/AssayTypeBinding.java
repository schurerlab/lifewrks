package edu.miami.ccs.life.config;

/**
 * @author Sam Abeyruwan
 * @version 0.1
 */
public class AssayTypeBinding {
    private String bioAssayType;
    private String bindsTo;

    public AssayTypeBinding() {
    }

    public String getBioAssayType() {
        return bioAssayType;
    }

    public void setBioAssayType(String bioAssayType) {
        this.bioAssayType = bioAssayType;
    }

    public String getBindsTo() {
        return bindsTo;
    }

    public void setBindsTo(String bindsTo) {
        this.bindsTo = bindsTo;
    }
}
